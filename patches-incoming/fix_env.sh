#!/bin/bash

# Создаем .env из твоего env, чтобы библиотека python-dotenv его увидела
cp env .env

# Добавляем/переопределяем модель на гарантированно рабочую с инструментами
# Мы используем qwen2.5:7b-instruct, так как она у тебя уже скачана (судя по логам)
echo "" >> .env
echo "# Принудительно устанавливаем рабочую модель для инструментов" >> .env
echo "TOOLS_MODEL=qwen2.5:7b-instruct" >> .env
echo "OLLAMA_MODEL=qwen2.5:7b-instruct" >> .env

echo "✅ Файл .env успешно создан/обновлен."
echo "🤖 Установлена модель: qwen2.5:7b-instruct"
echo "🚀 Теперь можешь перезапускать бота!"
